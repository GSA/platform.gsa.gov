<h4>Cookies and SupportTheVoter.gov</h4>

<p>What is a Cookie? Whenever you visit a website the website sends a small piece of information called a cookie along with the webpage. The main purpose of a cookie is to identify and customize webpages for you.</p>

<p>There are two kinds of cookies. A session cookie is a line of text that is stored temporarily in your computer’s random access memory (RAM). A session cookie is never written to a drive, and it is destroyed as soon as you close your browser. A persistent cookie from a website is saved to a file on your hard drive and is called up the next time you visit that website. This lets the website remember what you were interested in the last time you visited.</p>

<p>Use of Cookies on SupportTheVoter.gov: The SupportTheVoter.gov site creates only a session cookie when you visit and destroys it as soon as you close your browser. There are no persistent cookies created on SupportTheVoter.gov. If you have any questions about these policies, please contact us.</p>

